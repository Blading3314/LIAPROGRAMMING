/**
 * The Gender enumeration represents the possible gender identities
 * that can be assigned to an athlete in this program.
 * 
 * This helps categorize athletes and allows the program to store
 * and display gender information in a consistent way.
 * 
 * You can choose from:
 * - MALE
 * - FEMALE
 * - OTHER (for any identity not covered by the first two options)
 */
public enum Gender
{
    MALE, FEMALE, OTHER
}
